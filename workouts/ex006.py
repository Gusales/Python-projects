# Crie um algoritmo que leia um número e mostre seu dobro, seu triplo e sua raiz quadrada # noqa: E501
num = int(input('Digite um valor '))
doub = num**2
trip = num**3
raiz = num**(1/2)

print(f'O dobro desse número é {doub} \nO triplo desse número é {trip} \nA raiz quadrada desse valor é {raiz}')  # noqa: E501
